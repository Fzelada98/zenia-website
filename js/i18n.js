function detectLanguage() {
  var stored = localStorage.getItem('zenia-lang');
  if (stored && translations[stored]) return stored;
  var bl = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
  return bl === 'es' ? 'es' : 'en';
}

